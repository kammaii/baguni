package net.awesomekorean.podo.lesson;

public class IntermediateItems {

    private String dialog;
    private String dialogEng;
    private int peopleImage;
    private boolean isExpanded = false;


    public int getPeopleImage() {
        return peopleImage;
    }

    public void setPeopleImage(int peopleImage) {
        this.peopleImage = peopleImage;
    }

    public String getDialog() {
        return dialog;
    }

    public void setDialog(String dialog) {
        this.dialog = dialog;
    }

    public String getDialogEng() {
        return dialogEng;
    }

    public void setDialogEng(String dialogEng) {
        this.dialogEng = dialogEng;
    }

    public boolean getIsExpanded() {
        return isExpanded;
    }

    public void setIsExpanded(boolean isExpanded) {
        this.isExpanded = isExpanded;
    }
}
